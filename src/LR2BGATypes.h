//------------------------------------------------------------------------------
// LR2BGATypes.h
// LR2 BGAフィルタで使用される共通の型定義および列挙体定義
//------------------------------------------------------------------------------
#pragma once

#include <windows.h>
#include <strmif.h>

extern HINSTANCE g_hInst;

//------------------------------------------------------------------------------
// 動作モード (Operation Modes)
// フィルタの動作挙動を定義します。
//------------------------------------------------------------------------------
enum FilterMode {
    MODE_PASSTHROUGH = 0,   // パススルーモード (処理を行わずそのまま通過)
    MODE_RESIZE,            // リサイズモード (指定サイズへリサイズ)
    MODE_EXTERNAL_RENDER    // 外部レンダリングモード (ダミー出力を行い、ウィンドウへは別途描画)
};

//------------------------------------------------------------------------------
// リサイズアルゴリズム (Resize Algorithms)
// 画像拡大・縮小時に使用される補間アルゴリズム
//------------------------------------------------------------------------------
enum ResizeAlgorithm {
    RESIZE_NEAREST = 0,     // 最近傍補間（ニアレストネイバー）: 高速だがジャギーが目立つ（ドット絵向き）
    RESIZE_BILINEAR         // 双線形補間（バイリニア）: 滑らかに補間される（一般的）
    // Future: RESIZE_BICUBIC, RESIZE_LANCZOS などが必要であればここに追加
};


